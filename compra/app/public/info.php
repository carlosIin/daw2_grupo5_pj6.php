    <h1>Informació sobre l'aplicació <em>Compra</em></h1>

    <h2>Com funciona la cistella</h2>
    <p>
        Quan t'has validat com a client i accedeixes al teu <strong>Dashboard</strong>,
        pots entrar a l'opció <strong>"Gestionar cistella"</strong>.
    </p>
    <ul>
        <li>Pots indicar la quantitat de cada producte que vols afegir.</li>
        <li>En prémer el botó <strong>"Desar cistella"</strong>, es guarda la teva cistella al servidor.</li>
        <li>En prémer <strong>"Esborrar cistella"</strong>, s'elimina el contingut de la cistella.</li>
    </ul>

    <h2>Com funcionen les comandes</h2>
    <p>
        A partir de la cistella, el sistema pot generar <strong>comandes</strong> associades al teu usuari.
    </p>
    <ul>
        <li>Les comandes es guarden en fitxers a la teva carpeta personal de client.</li>
        <li>Des de l'opció <strong>"Veure comandes"</strong> del Dashboard pots consultar l'històric de compres.</li>
    </ul>

    <h2>Com validar-se (login) i sortir (logout)</h2>
    <p>
        Per poder utilitzar la cistella i consultar les teves comandes, és necessari que et validis
        com a client.
    </p>
    <ul>
        <li>Des de la pàgina d'inici fes clic a <strong>"Login"</strong>.</li>
        <li>Introdueix el teu <strong>nom d'usuari</strong> i la <strong>contrasenya</strong>.</li>
        <li>Si les dades són correctes, accediràs al teu <strong>Dashboard de client</strong>.</li>
        <li>Per sortir de l'aplicació, utilitza l'enllaç <strong>"Tancar sessió"</strong> del Dashboard.</li>
    </ul>

